package com.sghy1801.entity;

public class Yuce {
    private int id;
    private String exponentname;//指数名
    private String exponentnum;//指数值
    private String exponenttips;//提示


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getExponentname() {
        return exponentname;
    }

    public void setExponentname(String exponentname) {
        this.exponentname = exponentname;
    }

    public String getExponentnum() {
        return exponentnum;
    }

    public void setExponentnum(String exponentnum) {
        this.exponentnum = exponentnum;
    }

    public String getExponenttips() {
        return exponenttips;
    }

    public void setExponenttips(String exponenttips) {
        this.exponenttips = exponenttips;
    }
}
